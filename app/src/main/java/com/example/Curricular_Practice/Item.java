package com.example.Curricular_Practice;

public class Item {
    private String name;
    private String content;

    public Item() {
        name = "안승재";
        content = "장장";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
